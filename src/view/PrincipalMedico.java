    private void btnConsultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultasActionPerformed
        // TODO add your handling code here:
        
        LocalDate dataBusca = LocalDate.parse(comboData.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        
        ArrayList<Consulta> consultas = LoginForm.medicoLogado.getMinhasConsultas();
        
        DefaultTableModel tabelaConsultas = (DefaultTableModel) tableConsultas.getModel();
        
        tabelaConsultas.setNumRows(0);
        
        for(int i = 0; i < consultas.size(); i++) {
            Consulta c = consultas.get(i);
            String dataHorario = c.getDataHorario().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm"));
            
            
            if(c.getDataHorario().toLocalDate().equals(dataBusca))
                tabelaConsultas.addRow(new Object[]{dataHorario, c.getPaciente().getNome()});
        }
    }//GEN-LAST:event_btnConsultasActionPerformed

    private void btnRealizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizaActionPerformed
        // TODO add your handling code here:
        
        
        // Para evitar que n√£o tenha nenhuma linha selecionada
        if(tableConsultas.getSelectedRow() >= 0) {
            String nomePaciente = tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 1).toString();

            int idxPaciente = 0;
            for(int i = 0; i < LoginForm.pacientesDb.size(); i++) {
                if(LoginForm.pacientesDb.get(i).getNome().equals(nomePaciente)) {
                    idxPaciente = i;
                    break;
                }
            }
            String dataHorario = tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 0).toString();
            new TelaConsulta(idxPaciente, dataHorario).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um paciente para a consulta", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRealizaActionPerformed

